package kr.ac.jbnu.se.tetris;
public enum Tetrominoes {
    NoShape,        // 빈 공간 (블록 없음)
    ZShape,         // Z 모양 블록
    SShape,         // S 모양 블록
    LineShape,      // 직선 블록
    TShape,         // T 모양 블록
    SquareShape,    // 사각형 블록
    LShape,         // L 모양 블록
    MirroredLShape  // L 모양 블록을 반전한 모양
}
